public class VowelOrConsonant {
    public static void main(String[] args) {
        String a = "Z";
        String Vowels = "aeiouAEIOU";
        if(Vowels.contains(a))
        {
            System.out.println("Vowel");
        }
        else{
            System.out.println("Consonant");
        }
    }
}
